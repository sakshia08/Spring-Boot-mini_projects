package com.example.formbinder.model;

import java.time.LocalDate;

public class User {
    private String name;
    private LocalDate dob;

    // Getters and setters
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public LocalDate getDob() {
        return dob;
    }

    public void setDob(LocalDate dob) {
        this.dob = dob;
    }
}

